/** ひらがなコードポイントにオフセットを加えてカタカナにする */
function toKatakana(s: string): string {
  return [...s].map(ch => {
    const code = ch.codePointAt(0)!
    // ひらがな範囲: U+3041–U+3096 → カタカナ: +0x60
    return code >= 0x3041 && code <= 0x3096
      ? String.fromCodePoint(code + 0x60)
      : ch
  }).join('')
}

